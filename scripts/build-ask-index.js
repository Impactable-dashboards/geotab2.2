/* Builds ask-index.js — the grounding corpus for "Ask the data".
   Extracts the real, visible text of every surface so the widget can only ever
   answer with content that is actually in the room. Re-run after content edits:
     node scripts/build-ask-index.js
*/
const fs = require('fs');
const path = require('path');
const ROOT = path.join(__dirname, '..');

const FILES = [
  ['index', 'Operating Room', '/'],
  ['executive', 'Executive Review', '/executive'],
  ['program', 'Program Performance', '/program'],
  ['account-signals', 'Account Signals', '/account-signals'],
  ['segments', 'Audience & Segments', '/segments'],
  ['creative', 'Message & Creative', '/creative'],
  ['orchestration', 'The Orchestration Model', '/orchestration'],
  ['field', 'My Accounts', '/field'],
  ['top25', 'Top 25 Exec Prep', '/top25'],
  ['detail/6sense-trend', '6sense Surge Report', '/detail/6sense-trend'],
  ['detail/channel-efficiency', 'Channel Efficiency', '/detail/channel-efficiency'],
  ['detail/segments-full', 'Audience Segments (detail)', '/detail/segments-full'],
  ['detail/creative-full', 'Messaging & Creative (detail)', '/detail/creative-full'],
  ['detail/campaign-performance', 'Campaign Performance', '/detail/campaign-performance'],
  ['detail/abm-report', 'ABM Intelligence Report', '/detail/abm-report'],
];

const DROP = [
  /^impact\s*able$/i, /^access password$/i, /^enter\b/i, /^incorrect password$/i,
  /^linkedin marketing partner$/i, /confidential$/i, /^← ?room$/i, /^← /, /^geotab$/i,
  /^prepared for/i, /^living system/i, /ask the data/i, /^→/, /^[\d\s/]+$/,
  /^intelligence layer ·/i, /^field ·/i, /^strategy spine/i, /^return to the operating/i,
];

function clean(html) {
  html = html.replace(/<head[\s\S]*?<\/head>/i, ' ')
             .replace(/<script[\s\S]*?<\/script>/gi, ' ')
             .replace(/<style[\s\S]*?<\/style>/gi, ' ')
             .replace(/<!--[\s\S]*?-->/g, ' ');
  html = html.replace(/<div id="gate"[\s\S]*?<\/div>\s*<\/div>\s*<\/div>/i, ' ');
  html = html.replace(/<\/(p|div|li|h1|h2|h3|h4|h5|h6|td|section)>/gi, '\n')
             .replace(/<br\s*\/?>/gi, '\n');
  html = html.replace(/<[^>]+>/g, ' ');
  html = html.replace(/&amp;/g, '&').replace(/&rsquo;|&#39;/g, "'").replace(/&middot;/g, '·')
             .replace(/&rarr;/g, '→').replace(/&times;/g, '×').replace(/&nbsp;/g, ' ')
             .replace(/&lt;/g, '<').replace(/&gt;/g, '>').replace(/&[a-z]+;/g, ' ');
  return html;
}
function tidy(s) {
  return s.replace(/\s+/g, ' ').replace(/\s+([,.;:%])/g, '$1').replace(/\(\s+/g, '(').replace(/\s+\)/g, ')').trim();
}

const out = [];
for (const [f, sl, src] of FILES) {
  let h; try { h = fs.readFileSync(path.join(ROOT, f + '.html'), 'utf8'); } catch (e) { continue; }
  // Candidate lines (keep short ones for now so headings can be merged).
  const raw = clean(h).split('\n').map(tidy).filter(function (s) {
    return s && /[a-z]/i.test(s) && s.length <= 400 &&
      !s.includes('{') && !s.includes('var(--') && !/function\s*\(/.test(s) &&
      !DROP.some(function (r) { return r.test(s); });
  });
  // Merge a short heading/label line into the sentence that follows it, so card
  // titles like "Programmatic" or "60-day no-pay pilot" stay searchable.
  const merged = [];
  for (let i = 0; i < raw.length; i++) {
    const s = raw[i], nx = raw[i + 1];
    if (s.length < 42 && !/[.!?]$/.test(s) && nx && nx.length >= 30 && nx.length <= 320) {
      merged.push((s.replace(/[·:,\s]+$/, '') + '. ' + nx).slice(0, 400)); i++;
    } else merged.push(s);
  }
  const seen = {};
  for (const s of merged) {
    if (s.length < 35 || s.length > 400) continue;
    const key = s.slice(0, 64).toLowerCase();
    if (seen[key]) continue; seen[key] = 1;
    out.push({ t: s, src: src, sl: sl });
  }
}

const banner = '/* GENERATED by scripts/build-ask-index.js — do not edit by hand. */\n';
fs.writeFileSync(path.join(ROOT, 'ask-index.js'),
  banner + 'window.__ASK_INDEX=' + JSON.stringify(out) + ';\n');
fs.writeFileSync(path.join(ROOT, 'ask-corpus.json'), JSON.stringify(out));
console.log('wrote ask-index.js + ask-corpus.json:', out.length, 'chunks,',
  (fs.statSync(path.join(ROOT, 'ask-index.js')).size / 1024).toFixed(1) + 'KB');
